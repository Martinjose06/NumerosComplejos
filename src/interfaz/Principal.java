    private void cmdCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdCalcularActionPerformed

        int op, n1 = 0, n2 = 0, n3 = 0, n4 = 0;
        NumerosComplejos  c1, c2, c3 = null;
        op = cmbOp.getSelectedIndex();
        int sw = 1;
        try {
        n1 = Integer.parseInt(txtNumeroUno.getText());
        } catch (Exception e ){
            Helper.mensaje(this, "El Numero Debe Ser Correcto","Error",2);
            sw = 0;
        }
        try {
        n2 = Integer.parseInt(txtNumeroDos.getText());
        }
        catch (Exception e ){
            Helper.mensaje(this, "El Numero Debe Ser Correcto","Error",2);
            sw = 0;
        }
        try{
        n3 = Integer.parseInt(txtNumeroTres.getText());
        }catch (Exception e ){
            Helper.mensaje(this, "El Numero Debe Ser Correcto","Error",2);
            sw = 0;
        }
        try {
        n4 = Integer.parseInt(txtNumeroCuatro.getText());
        }
        catch (Exception e ){
            Helper.mensaje(this, "El Numero Debe Ser Correcto","Error",2);
            sw = 0;
        }
        if (sw == 1) {
            
        
        c1 = new NumerosComplejos(n1,n2);
        c2 = new NumerosComplejos(n3,n4);
        
        
        switch (op){
            case 0:
                c3 = c1.Suma(c2);
            break;
            case 1:
                c3 = c1.Resta(c2);
            break;
            case 2:
                c3 = c1.Multiplicacion(c2);
            break;
            case 3:
                c3 = c1.division(c2);
            break;
        }
        }
        
        txtNumeroCinco.setText(""+c3.getNumerouno());
        txtNumeroSeis.setText(""+c3.getNumerodos());
        
    }//GEN-LAST:event_cmdCalcularActionPerformed

